# Escape sequence are characters and never get printed as a part of output.

print("It's alright.")  # \'	Single Quote
print('He said,"Hello World"')  # \"	Double Quote
print("This will insert one \\ (backslash).")  # \\	Backslash
print("Hello\nWorld!")  # \n	New Line
print("Hello\tWorld!")  # \t	Tab
print("Hello \bWorld!")  # \b	Backspace (It erase one character)
